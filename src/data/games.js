/**
 * Utility function to get a game by slug
 */
export function getGameBySlug(slug) {
  return games.find(game => game.slug === slug);
}

/**
 * Utility function to get games by category
 */
export function getGamesByCategory(categoryId) {
  return games.filter(game => game.categories.includes(categoryId));
}

/**
 * Utility function to get featured games
 */
export function getFeaturedGames() {
  return games.filter(game => game.featured);
}

/**
 * Utility function to search games by title
 */
export function searchGames(query) {
  const lowercaseQuery = query.toLowerCase();
  return games.filter(game => 
    game.title.toLowerCase().includes(lowercaseQuery) || 
    game.description.toLowerCase().includes(lowercaseQuery)
  );
} 